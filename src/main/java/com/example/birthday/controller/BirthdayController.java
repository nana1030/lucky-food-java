    @PostMapping("/birthday")
    public ResponseEntity<?> receiveBirthday(
            @Valid @RequestBody BirthdayRequest request,
            BindingResult bindingResult) {
 
        if (bindingResult.hasErrors()) {
            String errorMessage = bindingResult.getAllErrors()
                    .get(0).getDefaultMessage();
            return ResponseEntity.badRequest()
                    .body(new BirthdayResponse(false, errorMessage, 0, "", null));
        }
 
        LocalDate birthdate = request.getBirthdate();
        LocalDate today     = LocalDate.now();
 
        int age = Period.between(birthdate, today).getYears();
 
        LocalDate nextBirthday = birthdate.withYear(today.getYear());
        if (!nextBirthday.isAfter(today)) {
            nextBirthday = nextBirthday.plusYears(1);
        }
        String nextBirthdayStr = nextBirthday.format(
                DateTimeFormatter.ofPattern("yyyy年MM月dd日"));
 
        int kanshiIndex = calcKanshiIndexByDay(birthdate);
        String[] kanshi = KANSHI_MAP.get(kanshiIndex);
 
        String message = String.format("受信成功！%s生まれの%d歳です。", birthdate, age);
 
        return ResponseEntity.ok(
                new BirthdayResponse(true, message, age, nextBirthdayStr, kanshi));
    }
 
    private int calcKanshiIndex(int year) {
        // 1984年 = 甲子(index 0) を基準に計算
    	return ((year - 1953) % 60 + 60) % 60;
    }
    private int calcKanshiIndexByDay(LocalDate date) {
        LocalDate base = LocalDate.of(1900, 1, 1); // 甲戌(index 10)基準
        long days = java.time.temporal.ChronoUnit.DAYS.between(base, date);
        return (int)(((days + 10) % 60 + 60) % 60);
    }
